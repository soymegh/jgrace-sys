import axios from "axios";
export default {
    obtenerTodosTiposCuenta(cb, errorCb) {
        axios.get('contabilidad/tipos-cuenta/obtener-tipos-cuenta-todas')
            .then(function (response) {
                if (response.data.status === 'success') {
                    cb(response.data.result)
                } else {
                    errorCb(response.data.result)
                }
            })
            .catch(function (error) {
                errorCb(error)
            })
    },
    obtenerTiposCuenta(data, cb, errorCb) {
        axios.post('contabilidad/tipos-cuenta/obtener-tipos-cuenta', data)
            .then(function (response) {
                if (response.data.status === 'success') {
                    cb(response.data.result)
                } else {
                    errorCb(response.data.result)
                }
            })
            .catch(function (error) {
                errorCb(error)
            })
    },
    obtenerTipoCuenta(data, cb, errorCb) {
        axios.post('contabilidad/tipos-cuenta/obtener-tipo-cuenta-contable', data)
            .then(function (response) {
                if (response.data.status === 'success') {
                    cb(response.data.result)
                } else {
                    errorCb(response.data.result)
                }
            })
            .catch(function (error) {
                errorCb(error)
            })
    },

    actualizar(data, cb, errorCb) {
        axios.put('contabilidad/tipos-cuenta/actualizar', data)
            .then(function (response) {
                if (response.data.status === 'success') {
                    cb(response.data.result)
                } else {
                    errorCb(response.data.result)
                }
            })
            .catch(function (error) {
                errorCb(error)
            })
    },

}